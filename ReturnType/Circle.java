package ReturnType;

public class Circle extends Shape {
 private int radius;
 public static final double PI= 3.14;

 public Circle(){
	}
		public Circle(int r){
			this.radius = radius ;
		}
			
			public int getRadius(){
			return radius;
		
		}
		
		public double area (){
		double arr = PI*radius*radius;
		return arr;
}
}
